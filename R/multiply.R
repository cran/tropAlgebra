#'   Product: Tropical Algebra Function.
#'  This function returns the product of X and Y according to tropical algebra.
#' @param  x a number.
#'  @param    y a number.
#'
#'  @return The product of x and y.
#'
#'  @examples
#'  multiply(6,5)
#'
#'   e.g.
#'   x<- 6
#'   y<- 5
#'   Now function call : multiply(5,6)
#'   The returned value will be 11.
#'
#'
#'@export
multiply<-function(x,y){
  return (x+y)
}
